//! Comparison Operators

// 1. Greater than                 >       // L Value R se bari he ?
// 2. Less than                    <       // L Value R se choti he ?
// 3. Greater than or Equal to     >=      // L Value R se bari ya brabar he ?
// 4. Less than or Equal to        <=      // L Value R se choti ya brabar he ?
// 5. Equal to                     ==      // Values brabar hain ?
// 6. Strict Equal to              ===     // Values aur Type brabar hain ?
// 7. Not Equal                    !=      // Values brabar nahi hain ?
// 8. Strict Not Equal             !==     // Values aur Type brabar nahi hain ?

// Examples

// 1. Greater than
// var a = 5;
// var b = 4;
